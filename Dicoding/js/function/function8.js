// // * FUNCTION DECLARATION VS FUNCTION EXPRESSION *


// *Function Declaration*
// function indentifier (parameterList_opt) {functionBody}


function tampilPesan (nama) {
    alert('halo' + nama);
}


// *Function Expression*
// function indentifier_opt (parameterList_opt) {functionBody}

var tampilPesan = function (nama) {
    alert('helo' + nama) 
}
